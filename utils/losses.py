@torch.no_grad()
def update_ema_variables(model, ema_model, alpha):
    for ema_param, param in zip(ema_model.parameters(), model.parameters()):
        ema_param.data.mul_(alpha).add_((1 - alpha) * param.data)
        

class CustomLoss(nn.Module):
    def __init__(self, class_weights=None, smooth=1e-6):
        """
        Initialize the custom loss function.

        Args:
        - class_weights (list or tensor): Weights for the classes [BGR, RV, MYO, LV].
        - smooth (float): Smoothing term for numerical stability.
        """
        super(CustomLoss, self).__init__()
        if class_weights is None:
            class_weights = [1, 2, 4, 1]
        self.class_weights = torch.tensor(class_weights, dtype=torch.float32)
        self.smooth = smooth

    def soft_dice_loss(self, Y, T):
        """
        Compute the Soft Dice Loss.

        Args:
        - Y (tensor): Predicted segmentation map (batch, classes, H, W).
        - T (tensor): Ground truth segmentation map (batch, classes, H, W).

        Returns:
        - loss (tensor): Scalar Soft Dice Loss.
        """
        num_classes = Y.shape[1]
        loss = 0
        for c in range(num_classes):
            y_pred_c = Y[:, c]
            y_true_c = T[:, c]
            intersection = torch.sum(y_pred_c * y_true_c)
            union = torch.sum(y_pred_c**2 + y_true_c**2)
            dice_loss = 1 - (2 * intersection + self.smooth) / (union + self.smooth)
            loss += self.class_weights[c] * dice_loss
        return loss

    def absent_class_penalty(self, Y, T):
        """
        Penalize predictions for absent classes.

        Args:
        - Y (tensor): Predicted segmentation map (batch, classes, H, W).
        - T (tensor): Ground truth segmentation map (batch, classes, H, W).

        Returns:
        - penalty (tensor): Scalar penalty loss.
        """
        penalty = 0
        for c in range(1, 4):  # Penalize only RV, MYO, LV
            T_class_absent = torch.all(torch.all(T[:, c] == 0, dim=1), dim=1)
            if T_class_absent.any():
                penalty += torch.sum(-torch.log(1 - Y[T_class_absent, c] + self.smooth))
        return penalty

    def lv_boundary_penalty(self, Y):
        """
        Penalize Left Ventricle adjacency to Background and RV.

        Args:
        - Y (tensor): Predicted segmentation map (batch, classes, H, W).

        Returns:
        - penalty (tensor): Scalar penalty loss.
        """
        penalty = 0
        lv_preds = Y[:, 3]  # LV predictions
        bgr_rv_preds = Y[:, 0] + Y[:, 1]  # Background and RV predictions

        # Compute adjacency penalty for each pixel
        adjacency_penalty = lv_preds * F.conv2d(bgr_rv_preds.unsqueeze(1), 
                                                weight=torch.ones(1, 1, 3, 3).cuda(), 
                                                padding=1)
        penalty += torch.sum(adjacency_penalty)
        return penalty

    def forward(self, Y, T):
        """
        Compute the final loss.

        Args:
        - Y (tensor): Predicted segmentation map (batch, classes, H, W).
        - T (tensor): Ground truth segmentation map (batch, classes, H, W).

        Returns:
        - total_loss (tensor): Scalar total loss.
        """
        # Y = F.softmax(Y, dim=1)  # Apply softmax to predictions
        T_onehot = F.one_hot(T, num_classes=Y.shape[1]).permute(0, 3, 1, 2).float()
        dice_loss = self.soft_dice_loss(Y, T_onehot)
        absent_class_loss = self.absent_class_penalty(Y, T_onehot)
        lv_boundary_loss = self.lv_boundary_penalty(Y)

        total_loss = dice_loss + absent_class_loss + lv_boundary_loss
        return total_loss
